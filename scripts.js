let paymentContract = new web3.eth.Contract(paymentContractABI, paymentContractAddress);
let userContract = new web3.eth.Contract(userContractABI, userContractAddress);
let userAccount;

async function connectWallet() {
    const accounts = await window.ethereum.request({ method: 'eth_requestAccounts' });
    userAccount = accounts[0];
}

async function deposit() {
    const amount = web3.utils.toWei(document.getElementById('depositAmount').value, 'ether');
    await paymentContract.methods.deposit().send({ from: userAccount, value: amount });
}

async function sendEther() {
    const amount = web3.utils.toWei(document.getElementById('sendAmount').value, 'ether');
    const recipient = document.getElementById('recipientAddress').value;
    const message = document.getElementById('sendMessage').value;
    await paymentContract.methods.send(recipient, amount, message).send({ from: userAccount });
}

